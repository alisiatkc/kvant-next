  if (q.includes('паспорт') || (q.includes('заполн') && q.includes('проект')) || q.includes('документ') && q.includes('проект')) {
    return 'Паспорт проекта — основной документ КОП, заполняется в личном кабинете.\n\nСодержит:\n• Название проекта и трек практики\n• Описание КОП: цель, целевая аудитория, особенности\n• Состав команды — капитан и участники\n• Производственный файл (DXF, STL, SVG или PDF)\n\nПаспорт обязателен для публикации в каталоге — без него кнопка «Опубликовать» не сработает. Куратор также проверяет паспорт при ревью.'
  }

