    return 'КОП — Коробочный образовательный комплект. Это готовое образовательное решение, которое любой учитель может взять и применить на уроке без дополнительной подготовки.\n\nВ состав входит:\n• Сам продукт — игра, модель, манипулятив или пособие\n• Инструкция по использованию\n• Методические материалы для педагога\n\nКОП создаётся студентами в технопарке с использованием реального оборудования, проходит апробацию в образовательных учреждениях и публикуется в открытом каталоге.'
